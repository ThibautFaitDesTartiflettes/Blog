    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Category::create([
            'name' => $request->name,
        ]);

        return redirect()->route('Admin')->with('Article', 'Catégorie ajoutée avec succès');
    }

    /**
     * Display the specified resource.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function show(Request $request)
    {
        if ($request->input('submit') == 'modify') {
            
            $selectedCat = Category::find($request->input('id'));
            return back()->with('selectedCat', $selectedCat);

        } elseif ($request->input('submit') == 'delete') {

            $id = $request->input('id');
            Category::destroy($id);
            return back()->with('Article', 'Article supprimé avec succès');
        }
    }

